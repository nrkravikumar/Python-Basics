##List:
##      => It can be represented as "[]"
##      => It allows duplicate data
##      => It can changes the values
##      => Slicing can be done because of index values
##      => It can be typecasted as "list()"
##      => All Datatype of elements can be stored
##      => It store the data in order format 

j = ['Manoj',4,2.012,True,'45.00',4,4,45]
##print(j,type(j))

##Slicing:
##      variablename => all data type elements
##      [:] => Start 0 and ending len-1
##           => All Data type elements
##      [::] => start value and ending len-1 and step value

print(j)
print(j[:])
##single parameters
print(j[5])
print(j[-1])
##two parameters
print(j[2:])
print(j[-3:])
print(j[:3])
print(j[:-2])
print(j[2:4])
print(j[1:-3])
print(j[4:-2])
print(j[-3:6])
print(j[-6:-3])
print(j[-3:-5])





