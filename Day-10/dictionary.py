##Dictionary:
##========
##      - It can be represented as "{}"
##      - It stores values in key and value pairs
##      - Keys cant store duplicate values but values can store duplicates
##      - It can be typecasted as dict()
##      - Slicing can be done for values but not for keys
##      

g = {('name','prasad'),('age','20'),('sal',25000)}
f = {'name':'Manoj','age':20,'sal':30000}
print(g,type(g))
print(f,type(f))

